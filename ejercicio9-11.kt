fun main() {
    // Números enteros
    val entero1 = 10
    val entero2 = 3
    
    // Suma
    println("Suma de enteros: ${entero1 + entero2}")
    
    // Resta
    println("Resta de enteros: ${entero1 - entero2}")
    
    // Multiplicación
    println("Multiplicación de enteros: ${entero1 * entero2}")
    
    // División
    println("División de enteros: ${entero1 / entero2}")
    
    // Números flotantes
    val flotante1 = 10.5
    val flotante2 = 3.2
    
    // Suma
    println("Suma de flotantes: ${flotante1 + flotante2}")
    
    // Resta
    println("Resta de flotantes: ${flotante1 - flotante2}")
    
    // Multiplicación
    println("Multiplicación de flotantes: ${flotante1 * flotante2}")
    
    // División
    println("División de flotantes: ${flotante1 / flotante2}")
}
